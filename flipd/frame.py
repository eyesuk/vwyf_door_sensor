class Frame:

    def __init__(self, ad=0xff):
        self.wdth = 28
        self.hght = 7
        self.b = bytearray([
            0x80,  # header
            0x83,  # 28 bytes refresh
            int(ad), # address
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, # 28 bytes data
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 
            0x8F # EOT
        ])

    def __setitem__(self, i, b):
        self.b[i+3] = b
